package lista_05_03_2018.exercicio3;

public class Lance {
    private String autor;
    private double valor;

    public Lance(String autor, double valor) {
        this.autor = autor;
        this.valor = valor;
    }

    public String getAutor() {
        return autor;
    }

    public double getValor() {
        return valor;
    }
}
